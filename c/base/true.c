int main ()
{
unsigned int a=6;
int b=-20;
(a+b)?puts(">6"):puts("<=6");
}

/*
这个笔试题目考察的是C语言真值的定义与三目运算;
C 语言中 一切非“0”的逻辑量其逻辑值都为真，a+b = -14 属于非0,故为真，不要被后面的输出结果所迷惑！

在LINUX下的约定中，返回值 0 则表示 true 真，返回非 0 则为 false ，可以理解为返回成功的状态只有一个，而出现错误的情况不只是一种！

想打印变量在内存中的十六进制值，可以在程序中插入如下语句：
printf("a is %x\n",a);
printf("b is %x\n",b);
printf("x %x\n",(a+b));
printf("d %d\n",(a+b)); 打印整形数值;
printf("u %u\n",(a+b)); 打印无符号类型数值;

*/
